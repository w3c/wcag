   <h1>Mobile Accessibility Task Force (Mobile A11Y TF) Work Statement</h1>
   <p> The <a href="http://www.w3.org/WAI/GL/mobile-a11y-tf/">Mobile Accessibility Task Force</a> (Mobile A11Y TF) is a task force of the <a href="http://www.w3.org/WAI/GL/">Accessibility Guidelines Working Group (AG WG)</a> and, before its closure, the <a href="http://www.w3.org/WAI/UA/">User Agent Accessibility Guidelines Working Group (UAWG)</a>. It assists this Working Group with the work identified below.</p>
