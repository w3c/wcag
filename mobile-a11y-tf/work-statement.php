   <div>
      <h2 id="dependencies">Dependencies</h2>
      <p>The Mobile Accessibility Task Force depends on the <a href="http://www.w3.org/WAI/GL/">AG WG</a> for the following reasons:</p>
      <ul>
         <li>to ensure accurate interpretation and representation of WCAG requirements;</li>
         <li>utilize the UAWG's previous work that addresses the mobile space;</li>
         <li>to leverage the development of HTML 5 and ARIA techniques for mobile uses.</li>
      </ul>
      <p>The Mobile Accessibility Task Force will also coordinate with the <a href="http://www.w3.org/WAI/APA/">Accessible Platform Architectures Working Group (APA WG)</a>, for technical advice and <a href="http://www.w3.org/WAI/EO/">Education and Outreach Working Group (EOWG)</a> where necessary to improve the writing, presentation, and educational value of the developed materials.</p>
   </div>
   <div>
      <h2 id="communication">Communication</h2>
      <p>Mobile Accessibility Task Force communications are publicly visible. Communication mechanisms for the Mobile Accessibility Task Force include:</p>
      <ul>
         <li>Mobile Accessibility Task Force mailing list and <a href="http://lists.w3.org/Archives/Public/public-mobile-a11y-tf/">mailing list archive</a> ;</li>
					<li>Use of a <a href="wiki/">wiki</a> to share development of resources;</li>
					<li><a href="https://www.w3.org/2002/09/wbs/66524/">Web-based surveys</a> to poll group opinon;</li>
					<li>IRC discussion on the <a href="irc://irc.w3.org/mobile-a11y">#mobile-a11y</a> IRC channel, used largely for minute-taking;</li>
         <li>Weekly teleconferences, <a href="minutes">minutes</a> from the teleconferences are also sent to WCAG WG mailing lists;</li>
         <li>Regular updates and feedback to the WCAG WG, and potentially the WAI CC as needed;</li>
         <li>Regular submission of deliverables to the WCAG WG for approval;</li>
         <li>Summary of current status and progress on the <a href="./">Mobile Accessibility Task Force home page</a>.</li>
      </ul>
   </div>
   <div>
      <h2 id="participation">Participation</h2>
      <p>To join the Mobile A11Y TF, individuals must be participants of the AG WG. Particpants are expected to actively contribute to the work of Mobile A11Y TF, including:</p>
      <ul>
         <li>Minimum 4 hours per week of Mobile A11Y TF work (this time also counts towards the individual's participation requirement in the sponsoring WG through which they have joined);</li>
         <li>Remain current on the Mobile Accessibility Task Force mailing list and respond in a timely manner to postings;</li>
         <li>Participate in Mobile Accessibility Task Force telephone meetings, or send regrets to the Mobile Accessibility Task Force mailing list.</li>
      </ul>
      <p>Contact <a href="mailto:joshue.oconnor@cfit.ie,akirkpat@adobe.com?subject=Mobile%20Accessibility%20Task%20Force%20Enquiry">Joshue O Connor, Andrew Kirkpatrick</a> to become a participant of Mobile Accessibility Task Force or with questions.</p>
