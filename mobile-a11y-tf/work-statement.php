      <h2 id="status">Status</h2>
      <p>This is a  Work Statement for the  Mobile A11Y Task Force (MATF). The minutes of the <a href="http://www.w3.org/2013/09/17-wai-wcag-minutes.html#item03">WCAG WG approval</a> and <a href="http://www.w3.org/2013/09/12-ua-minutes.html#item01">UAWG approval</a> of this work statement are public. The Task Force is active. Current work may be viewed on their <a href="http://www.w3.org/WAI/GL/mobile-a11y-tf/wiki/Main_Page">wiki</a>. The<a href="http://www.w3.org/WAI/GL/mobile-a11y-tf/"> home page of the Mobile Accessibility Task Force</a> has links to active information on the group. </p>
      <p style="background-color: yellow;">Note: this work statement has been updated to remove references to closed groups and change the name of the sponsoring Working Group. These changes have not yet been formally approved.</p>
   </div>
   <div>
      <h2 id="objective">Objective</h2>
      <p>The objective of the Mobile Accessibility Task Force is to produce techniques, understanding and guidance documents as well as updates to existing related W3C / WAI material that addresses the mobile space. This work includes developing (or updating):</p>
      <ul>
         <li> The creation of mobile techniques for WCAG using HTML5, ARIA, CSS and JavaScript (primarily the open web stack);</li>
         <li> The development of design guidance or mobile web accessibility best practices;</li>
         <li> Review of existing resources that may exist outside W3C space.</li>
      </ul>
   </div>
   <div>
      <h2 id="approach">Approach</h2>
      <p>Initially the Task Force will define the scope of work need to provide complete information on how to support mobile accessibility. The work will likely be broken down into modular components that can be used as independent resources or as part of a cohesive suite. The individual components of the Mobile A11Y Task Force's work may be developed as W3C Recommendations, W3C Working Group Notes, or other W3C / WAI resources. This will be decided after the development of an initial, more detailed set of requirements for handling accessibility in mobile environments.</p>
      <p>The work will be carried out iteratively with continual involvement of the public throughout the development. In particular, key stakeholders such as developers, evaluators, experts, researchers, and users will be regularly involved in the development process of the work of the Mobile Accessibility Task Force.</p>
   </div>
