    <h2 id="about">About the Low Vision Accessibility Task Force</h2>
    <p>The Low Vision Accessibility Task Force (LVTF) was created as a task force of the <a href="http://www.w3.org/WAI/GL/">Accessibility Guidelines Working Group (AG WG)</a> and, before it closure, the <a href="http://www.w3.org/WAI/UA/">User Agent Accessibility Guidelines Working Group (UAWG)</a>. The UAWG has closed.</p>
    <p>The objective of the Task Force is to address web accessibility issues   specific to people with low vision.  Task Force work  includes:</p>
    <ul>
      <li>Document accessibility requirements/needs for people with low vision</li>
      <li>Identify gaps between user needs and existing requirements in <a href="https://www.w3.org/WAI/intro/wcag">WCAG</a>, <a href="https://www.w3.org/WAI/intro/uaag">UAAG</a>, <a href="https://www.w3.org/WAI/intro/atag">ATAG</a></li>
      <li>Document sufficient techniques, advisory techniques, and failures for WCAG 2.0 related to low vision</li>
      <li>Develop an extension to WCAG 2.0 to address low vision</li>
      <li>Refine <a href="http://www.w3.org/TR/UAAG20-Reference/">user agent use cases</a> for low vision, as needed</li>
      <li>Develop support materials to explain the issues and implementation of the above resources effectively</li>
    </ul>
