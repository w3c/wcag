				</ol>
			</section>
		</section>
		<section>
			<h2>Cognitive</h2>
			<section>
				<h3>Attention</h3>
				<ol>
					<li><p>Use with limited ability to focus attention </p></li>
					<li><p>Use with limited ability to direct attention</p></li>
					<li><p>Use with limited ability to shift attention </p></li>
				</ol>
			</section>
			<section>
				<h3>Language &amp; Literacy</h3>
				<ol>
					<li><p>Use without ability to read </p></li>
					<li><p>Use with limited ability to recognize written language</p></li>
					<li><p>Use with limited ability to comprehend written language </p></li>
					<li><p>Use without ability to write </p></li>
					<li><p>Use with limited ability to correctly write (or type) words and use
          punctuation</p></li>
					<li><p>Use without understanding symbols</p></li>
					<li><p>Use without understanding metaphors, idioms, euphemisms, or specific dialect of culture
            or location</p></li>
				</ol>
			</section>
			<section>
				<h3>Learning</h3>
				<ol>
					<li><p>Use with limited ability of math and numeric concepts</p></li>
					<li><p>Use with limited compositional skill (simultaneous thinking and
        input)</p></li>
					<li><p>Use with limited coordinational skill (motoric skills, visual-spatial organizational
            memory, and social)</p></li>
				</ol>
			</section>
			<section>
				<h3>Memory</h3>
				<ol>
					<li><p>Use with limited short-term or working memory </p></li>
					<li><p>Use with limited medium or long-term memory</p></li>
					<li><p>Use with limited sensory memory</p>
					<ol>
						<li><p>Visual</p></li>
						<li><p>Visuo-spatial</p></li>
						<li><p>Auditory</p></li>
					</ol></li>
				</ol>
			</section>
			<section>
