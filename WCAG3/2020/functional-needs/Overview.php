			<h2>Sensory</h2>
			<section>
				<h3>Vision &amp; Visual</h3>
				<ol>
					<li><p>Use without vision</p>
					<ol>
						<li><p>Use as blind (born without vision)</p></li>
						<li><p>Use with blindness (acquired blindness during lifetime)</p></li>
					</ol></li>
					<li><p>Use with limited vision</p>
					<ol>
						<li><p>Use with limited central vision</p></li>
						<li><p>Use with limited peripheral vision ·XR·</p></li>
						<li><p>Use with limited interocular acuity or monocular input</p></li>
					</ol></li>
					<li><p>Use without color perception</p></li>
					<li><p>Use with limited color perception</p></li>
					<li><p>Use with limited depth perception ·XR·</p></li>
					<li><p>Use with limited orientation or spatial tracking ·XR·</p></li>
					<li><p>Use with photosensitivity (too much or too little)</p></li>
				</ol>
			</section>
			<section>
				<h3>Hearing &amp; Auditory</h3>
				<ol>
					<li><p>Use without hearing</p>
					<ol>
						<li><p>Use as Deaf (born with congenital deafness and/or to a deaf
          family)</p></li>
						<li><p>Use as deaf (acquired deafness during lifetime after a language was
            learned)</p></li>
					</ol></li>
					<li><p>Use with limited hearing</p></li>
					<li><p>Use with limited auditory processing (speech)</p></li>
					<li><p>Use with (age and Presbycusis related) sensorineural hearing loss (limited frequency
            range)</p></li>
				</ol>
			</section>
			<section>
				<h3>Sensory Intersections</h3>
				<ol>
					<li><p>Use without vision and hearing</p>
					<ol>
						<li><p>Use without vision from birth then without hearing as acquired </p></li>
						<li><p>Use without hearing from birth then without vision as
        acquired </p></li>
					</ol></li>
					<li><p>Use with vestibular issues</p></li>
					<li><p>Use without spatial auditory awareness or perception (needs diegetic sound)
            ·XR·</p></li>
				</ol>
			</section>
		</section>
		<section>
