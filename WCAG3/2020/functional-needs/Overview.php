					<li><p>Use with limited mobility ·XR·</p>
					<ol>
						<li><p>Use with limited ambulation</p></li>
						<li><p>Use with temporary or partial paralysis</p></li>
					</ol></li>
					<li><p>Use with limited reach or range ·XR·</p></li>
				</ol>
			</section>
			<section>
				<h3>Motor</h3>
				<ol>
					<li><p>Use without hands</p></li>
					<li><p>Use without multiple touchpoint gesture</p></li>
					<li><p>Use with limited strength</p></li>
					<li><p>Use without fine point control</p></li>
					<li><p>Use without physical tracking speed</p></li>
					<li><p>Use with tremors</p></li>
				</ol>
			</section>
			<section>
